    private void showData(){
        try{
            //tableMain.removeAll();
            String[] arr = {"Bus ID","Biển số","ID nhân viên"," Họ tên","Vị trí","Time ID","Bắt đầu","Kết thúc","Đến ngày"};
            DefaultTableModel model=  new DefaultTableModel(arr,0);
            
            Connection connection = DBConnection.getConnection();
            String query= "SELECT * FROM view_info_work order by  work_day desc , bus_id" ;
            PreparedStatement ps = connection.prepareStatement(query);
            ResultSet rs= ps.executeQuery();
            while(rs.next()){
                Vector vt= new Vector();
                vt.add(rs.getString("bus_id"));
                vt.add(rs.getString("license_plate"));
                vt.add(rs.getString("emp_id"));      
                vt.add(rs.getString("hoten"));
                vt.add(rs.getString("position"));
                vt.add(rs.getString("time_id"));
                vt.add(rs.getString("start_time"));
                vt.add(rs.getString("end_time"));
                vt.add(rs.getString("work_day"));
               
                model.addRow(vt);
    
        }
            DBConnection.closeConnection(connection);
            tableMain.setModel(model);  
        }catch(SQLException ex){
            Logger.getLogger(QLChuyen.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public class ComboItem {

    private Object value;
    private Object label;

    public ComboItem(Object value, Object label) {
        this.value = value;
        this.label = label;
    }

    public int getID() {
        return Integer.parseInt(value.toString());
    }

    public String getLabel() {
        return this.label.toString();
    }

    @Override
    public String toString() {
        return label.toString();
    }
}
    
    
    
