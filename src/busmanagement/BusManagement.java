          public ArrayList<Bus> getBusList(){
        ArrayList<Bus> buses = new ArrayList<>();
        Connection con = establishCon();
        Statement st =null;
        try {
            
            String busQueryAll = "SELECT * FROM BUS";
            st = con.createStatement();
            ResultSet rs = st.executeQuery(busQueryAll);
            while(rs.next())
            {
                Bus bus = new Bus(rs.getInt("bus_id"), rs.getInt("route_id"), rs.getInt("status"), rs.getString("license_plate"), rs.getString("bus_type"), rs.getDate("start_use"));
                buses.add(bus);
               
            }
            
        }
         catch (Exception e){
             e.printStackTrace();
         }
        finally{
            if(st !=null)
               {
                   try{
                       st.close();
                   }
                   catch (Exception e)
                   {
                       e.printStackTrace();
                   }
               }
               
              if(con !=null)
               {
                   try{
                       con.close();
                   }
                   catch (Exception e)
                   {
                       e.printStackTrace();
                   }
               }
        }
        return buses;
    
    }
          public void displayBusList()
    {
        ArrayList<Bus> buses = getBusList();
        DefaultTableModel model = (DefaultTableModel)busTable.getModel();
       
        Object [] rows = new Object[10];
        for(int i=0; i<buses.size(); i++)
        {
            
            rows[0] = buses.get(i).getBusId();
            rows[1] = buses.get(i).getRouteId();
          
            rows[2] = buses.get(i).getLicensePlate();
            rows[3] = buses.get(i).getBusType();
            rows[4] = buses.get(i).getStartUse();
            rows[5] = buses.get(i).getStatus();
            
            model.addRow(rows);
        }
    }
