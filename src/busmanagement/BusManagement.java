    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        int row = busTable.getSelectedRow();
        String cell = busTable.getModel().getValueAt(row, 0).toString();
        String deleteQuery = "DELETE FROM BUS WHERE bus_id= "+cell;
        Connection con = establishCon();
        Statement st =null;
        try{
            st = con.createStatement();
            st.executeUpdate(deleteQuery);
            JOptionPane.showMessageDialog(this, "Xóa thành công");
            DefaultTableModel dm = (DefaultTableModel)busTable.getModel();
            dm.removeRow(row);
            routeIdText.setText("");
        busTypeCombo.setSelectedItem("--Chọn loại xe--");
        statusCombo.setSelectedItem("Hoạt động");
        licensePlateText.setText("");
        swDayText.setText("");
        swMonthCombo.setSelectedItem("Chọn tháng");
        swYearText.setText("");
        deleteButton.setEnabled(false);
        updateButton.setEnabled(false);
         
        }
        catch(Exception e){
            e.printStackTrace();
            return;
        }
        finally{
            if(con!=null)
            {
                try{
                    con.close();
                }
                catch (Exception e){
                    e.printStackTrace();
                }
            }
            if(st != null)
            {
                try
                {
                    st.close();
                }
                catch (Exception e)
                {
                    e.printStackTrace();
                }
            }
        }

    }//GEN-LAST:event_deleteButtonActionPerformed

